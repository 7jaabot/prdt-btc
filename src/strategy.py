"""
strategy.py — Last-Second Momentum Strategy

Logic:
1. Monitor BNB price throughout the 5-minute window.
2. In the last `entry_window_seconds` of the window, compute P(Up).
3. Compare P(Up) to Polymarket's YES price.
4. If edge = |P(Up) - yes_price| > edge_threshold → signal a trade.
5. Size position using Kelly Criterion (fractional).

All calculations are pure functions for easy unit testing.
"""

import logging
import math
import time
from dataclasses import dataclass
from typing import Optional

import numpy as np

logger = logging.getLogger(__name__)


# ─────────────────────────────────────────────────────────────────────────────
# Data structures
# ─────────────────────────────────────────────────────────────────────────────

@dataclass
class Signal:
    """A trading signal generated by the strategy."""

    side: str                  # "YES" or "NO"
    edge: float                # Our edge over Polymarket's price
    p_up: float                # Our estimated P(BNB goes up)
    yes_price: float           # Polymarket's YES price at signal time
    kelly_fraction: float      # Raw Kelly fraction (before capping)
    position_size_usdc: float  # Recommended position size in USDC
    timestamp: float           # Signal generation time
    is_mock: bool = False      # True if based on mock data

    def __repr__(self):
        return (
            f"Signal({self.side} | edge={self.edge:.3f} | "
            f"P(Up)={self.p_up:.3f} vs PM={self.yes_price:.3f} | "
            f"size=${self.position_size_usdc:.2f})"
        )


@dataclass
class WindowInfo:
    """Information about the current PancakeSwap epoch / betting window."""

    window_start_ts: float     # Unix timestamp of round start (start_ts from contract)
    window_end_ts: float       # Unix timestamp of lock (lock_ts from contract — betting deadline)
    seconds_remaining: float   # Seconds until lock_ts (betting closes)
    window_index: int          # Epoch number (from contract)
    entry_window_seconds: float = 60.0  # How many seconds before lock is "entry window"

    @property
    def is_entry_window(self) -> bool:
        """True if we're in the last N seconds before lock (entry zone)."""
        return self.seconds_remaining <= self.entry_window_seconds

    @property
    def progress_pct(self) -> float:
        """How far through the betting window we are (0.0 → 1.0)."""
        duration = self.window_end_ts - self.window_start_ts
        if duration <= 0:
            return 1.0
        elapsed = time.time() - self.window_start_ts
        return min(1.0, elapsed / duration)


